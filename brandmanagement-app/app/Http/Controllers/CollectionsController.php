     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {

        $collection = new Collection;
        $collection->collection_name = request('collectionName');
        $collection->collection_season = request('collectionSeason');
        $collection->collection_description = request('collectionDescription');
        $collection->save();
        return redirect('/collections')->with('success', 'Collection Title Created');
    }

