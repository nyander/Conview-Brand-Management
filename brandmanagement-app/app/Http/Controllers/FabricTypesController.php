     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {

        $fabricName = request('fabricName');
        $fabrics = new FabricType();
        $fabrics->fabric_type_name = request('fabricName');
        $fabrics->description = request('description');
        $fabrics->save();

        return redirect('/fabrictypes')->with('success', $fabricName . 'has been added');
    }

