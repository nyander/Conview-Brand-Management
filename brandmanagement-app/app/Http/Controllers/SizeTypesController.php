     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $sizetype = new SizeType();
        $sizetype->gender = request('gender');
        $sizetype->size_type_name = request('sizeName');
        $sizetype->measurements = request('measurements');
        $sizetype->save();

        return redirect('/sizetypes')->with('success', 'Size Type Created');
    }

